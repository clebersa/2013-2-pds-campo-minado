package model;

/**
 * Define os tipos possíveis para um conteúdo.
 *
 * @author Cleber
 */
public enum TipoConteudo {

	/**
	 * Indica que o conteúdo é um número.
	 */
	NUMERO,
	/**
	 * Indica que o conteúdo é uma mina.
	 */
	MINA,
	/**
	 * Indica que não existe conteúdo, ou seja, ele é vazio.
	 */
	VAZIO;
}
